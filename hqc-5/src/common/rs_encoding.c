static inline
void bs_gf256_multab( uint32_t multab[8] , uint8_t b )
{
    uint32_t bx = b;
    multab[0] = bx;
    for(int i=1;i<8;i++) {
        uint32_t t1 = (bx>>7)&1;
        t1 *= 0x11d;
        bx = (bx<<1) ^ t1;
        multab[i] = bx;
    }
}

static inline
uint32_t bs_gf256_mul( uint32_t a , const uint32_t multab[8] ) {
    uint32_t res = 0;
    for(int i=0;i<8;i++) {
        uint32_t ai = (a>>i)&0x01010101;
        res ^= multab[i]*ai;
    }
    return res;
}


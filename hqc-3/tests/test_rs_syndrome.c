#define TEST_FUNC   rs_syndrome_n56_r32
#define PMAT   rs_parity_mat_n56_r32

#define N 56
#define K 24
#define P (N - K)
#define P_U32 ((P + 3) / 4)

// --- 測試主程式 ---
int main() {
    uint8_t codeword[N];
    SYNDROME_T syndrome[P];
    int all_passed = 1;

    printf("Starting RS (%d/%d) Parity Check Test (Weight-1 Vectors)...\n\n", N, K);

    for (int i = 0; i < N; i++) {
        // 1. 準備 input: 只有第 i 個 byte 是 1
        memset(codeword, 0, N);
        codeword[i] = 1;

        // 2. 執行編碼
        TEST_FUNC(syndrome, codeword);

        // 3. 驗證結果
        const uint8_t *expected_parity = (const uint8_t *)&PMAT[i * P_U32];
        
        int match = 1;
        // 檢查前 Parity 部分
        for (int j = 0; j < P; j++) {
            if (syndrome[j] != expected_parity[j]) {
                match = 0;
                break;
            }
        }
        if (match) {
            printf("[Test %02d] PASS (Input: 1 at index %d)\n", i, i);
        } else {
            printf("[Test %02d] FAIL!\n", i);
            printf("  Expected Parity: ");
            for(int k=0; k<P; k++) printf("%02x ", expected_parity[k]);
            printf("\n  Actual Parity:   ");
            for(int k=0; k<P; k++) printf("%02x ", syndrome[k]);
            printf("\n");
            all_passed = 0;
        }
    }

    if (all_passed) {
        printf("\nALL TESTS PASSED SUCCESSFULLY!\n");
    } else {
        printf("\nSOME TESTS FAILED. PLEASE CHECK YOUR IMPLEMENTATION.\n");
    }

    return 0;
}